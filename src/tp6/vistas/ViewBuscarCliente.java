    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String telefono = jtTelefono.getText();
        try {
            Cliente cliente = Menu.miDirectorio.buscarCliente(telefono);
            jtDni.setText(String.valueOf(cliente.getDni()));
            jtApellido.setText(cliente.getApellido());
            jtNombre.setText(cliente.getNombre());
            jtCiudad.setText(cliente.getCiudad());            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Cliente no encontrado");
        }
      
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        String telefono = jtTelefono.getText();
                           
            try {
                boolean borrado = Menu.miDirectorio.borrarCliente(telefono);
                if (borrado) {
                    JOptionPane.showMessageDialog(null,"Cliente eliminado");
                    limpiar();
                } else{
                    JOptionPane.showMessageDialog(null,"Cliente no encontrado");
                }                         
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"Error. Reintentar.");
            }
        
    }//GEN-LAST:event_btnBorrarActionPerformed
    
    public void limpiar() {
        jtDni.setText("");
        jtApellido.setText("");
        jtNombre.setText("");
        jtCiudad.setText("");
        jtTelefono.setText("");
    }

